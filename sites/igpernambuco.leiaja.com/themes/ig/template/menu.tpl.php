<script type="text/javascript">
  (function($) {
      $(document).ready(function() {
        
        $("#buscar").keyup(function(){
          // Mostrando a div de busca
          $("#searchOn").show();
          // PReenchendo os termos
          $(".termo-busca").text($(this).val());
        });
        
        $("#buscar, .searchOn").focus(function(){
          if($("#buscar").val() != "")
            $("#searchOn").show();
        });
        
        // Evento de click no box de busca
        $(".searchOn").click(function(){
          // caso seja IG Pernambuco/leiaja
          if($(this).attr("rel") == "pernambuco_content"){
            window.location.href = "http://www.leiaja.com/search/site/"+$("#buscar").val();
          }else{
            // Setando o escopo da busca
            $("#searchScope").val($(this).attr("rel"));
            
            // Disparando o click do botão de busca
            $(".botao-buscar").trigger("click");
          }
        });
        
        $("*").click(function(){
          if($(this).hasClass("buscar") != true)
            $("#searchOn").hide();
        });
        
      });
  })(jQuery);
